nbr = int(input("Enter a number to get the corresponding day of the week\n=> "))

if nbr == 1:
  print("Monday")
elif nbr == 2:
  print("Tuesday")
elif nbr == 3:
  print("Wednesday")
elif nbr == 4:
  print("Thursday")
elif nbr == 5:
  print("Friday")
elif nbr == 6:
  print("Saturday")
elif nbr == 7:
  print("Sunday")
else:
  print("Have you attended KG there are only 7 days!")
